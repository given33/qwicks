import { readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

const DEFAULT_SOURCE_ROOT = 'C:/Users/given/Desktop/pet/Action';
const DEFAULT_OUTPUT = 'src/shared/mqpet-original-action-index.ts';
const MOODS = new Set(['happy', 'peaceful', 'prostrate', 'sad', 'upset']);

const sourceRoot = path.resolve(process.argv[2] ?? DEFAULT_SOURCE_ROOT);
const outputPath = path.resolve(process.argv[3] ?? DEFAULT_OUTPUT);

function toPosix(value) {
  return value.split(path.sep).join('/');
}

function naturalParts(value) {
  return value.split(/(\d+)/).map((part) => (/^\d+$/.test(part) ? Number(part) : part.toLowerCase()));
}

function naturalCompare(a, b) {
  const left = naturalParts(a);
  const right = naturalParts(b);
  const len = Math.max(left.length, right.length);
  for (let index = 0; index < len; index += 1) {
    if (left[index] === undefined) return -1;
    if (right[index] === undefined) return 1;
    if (left[index] === right[index]) continue;
    if (typeof left[index] === 'number' && typeof right[index] === 'number') {
      return left[index] - right[index];
    }
    return String(left[index]).localeCompare(String(right[index]), 'en');
  }
  return 0;
}

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walk(fullPath));
    } else if (entry.isFile() && entry.name.toLowerCase().endsWith('.swf')) {
      files.push(fullPath);
    }
  }
  return files;
}

function classifyAction(folder, fileName) {
  const base = fileName.replace(/\.swf$/i, '');
  const lower = base.toLowerCase();
  const folderLower = folder.toLowerCase();

  if (folderLower === 'play') return 'play';
  if (folderLower === 'interact') return 'interact';
  if (folderLower === 'game') return 'game';

  if (lower.startsWith('appear')) return 'appear';
  if (lower.startsWith('bury')) return 'bury';
  if (lower.startsWith('clean')) return 'clean';
  if (lower.startsWith('cure')) return 'cure';
  if (lower === 'die') return 'die';
  if (lower.startsWith('dying')) return 'dying';
  if (lower.startsWith('dirty')) return 'dirty';
  if (lower.startsWith('eat')) return 'eat';
  if (lower.startsWith('enter')) return 'enter';
  if (lower.startsWith('exit')) return 'exit';
  if (lower.startsWith('first')) return 'first';
  if (lower.startsWith('hide')) return 'hide';
  if (lower.startsWith('hungry')) return 'hungry';
  if (lower.startsWith('levup')) return 'levelUp';
  if (lower.startsWith('revival')) return 'revive';
  if (lower.startsWith('sick')) return 'sick';
  if (lower.startsWith('speak')) return 'speak';
  if (lower.startsWith('stand')) return 'stand';
  if (lower === 'etoj' || lower === 'jtoc') return 'transition';
  return 'special';
}

function entryFromFile(fullPath) {
  const relativeFromAction = toPosix(path.relative(sourceRoot, fullPath));
  const parts = relativeFromAction.split('/');
  const [gender, sourceStage, ...rest] = parts;
  if (!['GG', 'MM'].includes(gender)) return null;
  if (!['Egg', 'Kid', 'Adult'].includes(sourceStage)) return null;

  const tail = rest.slice();
  let mood;
  if (sourceStage === 'Adult' && MOODS.has(tail[0])) {
    mood = tail.shift();
  }

  const fileName = tail[tail.length - 1];
  const folder = tail.length > 1 ? tail[tail.length - 2] : '';
  const sourcePath = `Action/${relativeFromAction}`;
  return {
    gender,
    sourceStage,
    ...(mood ? { mood } : {}),
    action: classifyAction(folder, fileName),
    name: fileName.replace(/\.swf$/i, ''),
    sourcePath,
    format: 'swf',
  };
}

const files = (await walk(sourceRoot)).sort((a, b) => naturalCompare(toPosix(a), toPosix(b)));
const entries = files.map(entryFromFile).filter(Boolean);
const entriesJson = JSON.stringify(entries, null, 2)
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$\{/g, '\\${');

const source = `// Generated by tools/mqpet-source/generate-action-index.mjs.
// Source: C:/Users/given/Desktop/pet/Action
/* eslint-disable */

export type OriginalQqpetActionFormat = 'swf';
export type OriginalQqpetActionGender = 'GG' | 'MM';
export type OriginalQqpetActionStage = 'Egg' | 'Kid' | 'Adult';
export type OriginalQqpetActionMood = 'happy' | 'peaceful' | 'prostrate' | 'sad' | 'upset';

export interface OriginalQqpetActionIndexEntry {
  gender: OriginalQqpetActionGender;
  sourceStage: OriginalQqpetActionStage;
  mood?: OriginalQqpetActionMood;
  action: string;
  name: string;
  sourcePath: string;
  format: OriginalQqpetActionFormat;
}

export const ORIGINAL_QQPET_ACTION_INDEX_META = {
  sourceRoot: 'C:/Users/given/Desktop/pet/Action',
  sourcePathPrefix: 'Action',
  totalAssets: ${entries.length},
} as const;

const ORIGINAL_QQPET_ACTION_ASSETS_JSON: string = \`${entriesJson}\`;

export const ORIGINAL_QQPET_ACTION_ASSETS = JSON.parse(ORIGINAL_QQPET_ACTION_ASSETS_JSON) as readonly OriginalQqpetActionIndexEntry[];
`;

await writeFile(outputPath, source, 'utf8');
console.log(`Generated ${entries.length} MQPet source action entries -> ${outputPath}`);
