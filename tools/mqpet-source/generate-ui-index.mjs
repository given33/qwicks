import { readdir, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';

const DEFAULT_SOURCE_ROOT = 'C:/Users/given/Desktop/pet';
const DEFAULT_OUTPUT = 'src/shared/mqpet-original-ui-index.ts';
const DOMAINS = ['Menu', 'stateInfo', 'windowTip', 'img_res', 'music'];
const ALLOWED_EXTENSIONS = new Set(['.bmp', '.gif', '.jpg', '.jpeg', '.mp3', '.png', '.svg']);

const sourceRoot = path.resolve(process.argv[2] ?? DEFAULT_SOURCE_ROOT);
const outputPath = path.resolve(process.argv[3] ?? DEFAULT_OUTPUT);

function toPosix(value) {
  return value.split(path.sep).join('/');
}

function naturalParts(value) {
  return value.split(/(\d+)/).map((part) => (/^\d+$/.test(part) ? Number(part) : part.toLowerCase()));
}

function naturalCompare(a, b) {
  const left = naturalParts(a);
  const right = naturalParts(b);
  const len = Math.max(left.length, right.length);
  for (let index = 0; index < len; index += 1) {
    if (left[index] === undefined) return -1;
    if (right[index] === undefined) return 1;
    if (left[index] === right[index]) continue;
    if (typeof left[index] === 'number' && typeof right[index] === 'number') return left[index] - right[index];
    return String(left[index]).localeCompare(String(right[index]), 'en');
  }
  return 0;
}

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walk(fullPath));
    } else if (entry.isFile() && ALLOWED_EXTENSIONS.has(path.extname(entry.name).toLowerCase())) {
      files.push(fullPath);
    }
  }
  return files;
}

async function entryFromFile(fullPath) {
  const relative = toPosix(path.relative(sourceRoot, fullPath));
  const parts = relative.split('/');
  const domain = parts[0];
  if (!DOMAINS.includes(domain)) return null;

  const info = await stat(fullPath);
  const extension = path.extname(fullPath).slice(1).toLowerCase();
  const name = path.basename(fullPath, path.extname(fullPath));
  return {
    domain,
    ...(parts.length > 2 ? { subdomain: parts[1] } : {}),
    name,
    sourcePath: relative,
    format: extension,
    bytes: info.size,
  };
}

const files = [];
for (const domain of DOMAINS) {
  files.push(...await walk(path.join(sourceRoot, domain)));
}
files.sort((a, b) => naturalCompare(toPosix(a), toPosix(b)));

const entries = (await Promise.all(files.map(entryFromFile))).filter(Boolean);
const totalBytes = entries.reduce((sum, entry) => sum + entry.bytes, 0);
const byDomain = Object.fromEntries(DOMAINS.map((domain) => [
  domain,
  entries.filter((entry) => entry.domain === domain).length,
]));
const entriesJson = JSON.stringify(entries, null, 2)
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$\{/g, '\\${');

const source = `// Generated by tools/mqpet-source/generate-ui-index.mjs.
// Source: C:/Users/given/Desktop/pet/{Menu,stateInfo,windowTip,img_res,music}
/* eslint-disable */

export type OriginalQqpetUiDomain = 'Menu' | 'stateInfo' | 'windowTip' | 'img_res' | 'music';
export type OriginalQqpetUiFormat = 'bmp' | 'gif' | 'jpg' | 'jpeg' | 'mp3' | 'png' | 'svg';

export interface OriginalQqpetUiIndexEntry {
  domain: OriginalQqpetUiDomain;
  subdomain?: string;
  name: string;
  sourcePath: string;
  format: OriginalQqpetUiFormat;
  bytes: number;
}

export const ORIGINAL_QQPET_UI_INDEX_META = {
  sourceRoot: 'C:/Users/given/Desktop/pet',
  sourcePathPrefixes: ${JSON.stringify(DOMAINS)},
  totalAssets: ${entries.length},
  totalBytes: ${totalBytes},
  byDomain: ${JSON.stringify(byDomain, null, 2)},
} as const;

const ORIGINAL_QQPET_UI_ASSETS_JSON: string = \`${entriesJson}\`;

export const ORIGINAL_QQPET_UI_ASSETS = JSON.parse(ORIGINAL_QQPET_UI_ASSETS_JSON) as readonly OriginalQqpetUiIndexEntry[];
`;

await writeFile(outputPath, source, 'utf8');
console.log(`Generated ${entries.length} MQPet original UI entries -> ${outputPath}`);
